<p style="font-size:25px;">Upon being diagnosed as COVID-19 positive by a government-authorized Centre, if the treatment is suggested at home, it will be covered for a maximum of 14 per incident provided:</p>
<ul class="data-lisitng" style="font-size:25px;">
<li>The doctor has advised the patient to undergo treatment at home</li>
<li>There is continuous monitoring of health status by a doctor for each day of home care treatment</li>
<li>Daily monitoring and maintenance of chart including records of treatment administered that is duly signed by the treating medical practitioner</li>
<li>The policy holder shall be allowed to avail the medical services as prescribed by the doctor</li>
<li>Under homecare expenses, both cashless &amp; reimbursement facility shall be permissible</li>
<li>If the policyholder takes the treatment in a non-network-hospital he can file for its reimbursement by taking prior approval from the insurance company before availing such services</li>
</ul>
<h4><strong>It shall cover:</strong></h4>
