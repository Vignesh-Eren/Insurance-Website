<li>Prescribed Medicines</li>
<li>Cost of diagnostic tests at a diagnostics Centre or home</li>
<li>Cost of oxygen, pulse oximeter, and Nebulizer</li>
<li>Doctor’s consultation charges</li>
<li>Nursing charges</li>
<li>Medical procedures limited to parenteral administration of medicines</li>
</ul>
<h3><b>AYUSH Treatment Expenses</b></h3>
<p style="font-size:25px;">The policy covers medical expenses incurred for inpatient COVID care treatment on positive diagnosis as per test results in a government authorized diagnostic centre. It also covers co-morbidity treatment along with the Coronavirus treatment up to the sum insured</p>
