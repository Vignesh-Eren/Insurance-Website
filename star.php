<h3><b>COVID Hospitalization Cover</b></h3>
<ul class="data-lisitng" style="font-size:25px;">
<li>Pre-hospitalization expenses are paid for 15 days before the date of admission</li>
<li>Post Hospitalization expenses are paid for 30 days after hospital discharge</li>
